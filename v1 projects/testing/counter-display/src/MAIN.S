.PC02

.INCLUDE "INSTRUCTIONS.MAC"
.INCLUDE "XM7SEG.MAC"

.SEGMENT "STARTUP"
_INIT:
	SEI
	CLC
	LDX #$FF
	TXS

	JSR XM7SEG_INIT
	XM7SEG_ON
	LDX #$00
	TXA
	TAY
	CLI

	JMP _MAIN

.SEGMENT "CODE"

_MAIN:

	LDA XM7SEG_BUFFER
	CLC
	ADC #$01
	STA XM7SEG_BUFFER
	LDA XM7SEG_BUFFER+1
	ADC #$00
	STA XM7SEG_BUFFER+1
	LDA XM7SEG_BUFFER+2
	ADC #$00
	STA XM7SEG_BUFFER+2
	XM7SEG_WORD
	JMP _MAIN
