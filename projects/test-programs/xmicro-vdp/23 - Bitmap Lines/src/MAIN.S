	WREG #%00000000, 45	;DIRECTION DOWN AND RIGHT
	WREG #%01110000, 46	;DRAW LINE, LOGICAL OPERATION - OVERWRITE

@1:	RSTAT 2			;WAIT UNTIL COMMAND IS COMPLETE
	AND #%00000001
	BEQ @1

	JMP _MAIN


.SEGMENT "CODE"

_MAIN:
NOPS 10
JMP _MAIN


VDP_ISR:
	RSTAT 00			;DETERMINE WHERE THE INTERRUPT CAME FROM
	AND #%10000000
	BEQ @1
	JMP V_INT
@1:	RSTAT 01
	AND #%00000001
	BEQ @2
	JMP H_INT
@2:	RTI

V_INT:
	RTI

H_INT:
	RTI

