;----PATTERN LAYOUT INIT----
PATTERN_INIT:
	WREG #%00000000, 14		;ZERO OUT VRAM
	MVA #%00000000, VDP+1
	MVA #%01000000, VDP+1		;SET UP ADDRESS ($0000), WRITE-MODE

	LDX #$00
	LDY #$FF
	LDA #$00

@1:	STA VDP+0
	DEX
	BNE @1
	DEY
	BNE @1

LOGO_COPY:
	MWA #LOGO_DATA, MOVE_FROM
	MWA #$2800, MOVE_SIZE

	WREG #%00000001, 14
 	MVA #%00000000, VDP+1
;	MVA #%01000000, VDP+1
	MVA #%01010110, VDP+1		;SET UP ADDRESS ($5600), WRITE-MODE

	LDY #00
	LDX MOVE_SIZE+1
	BEQ @MD2
@MD1:	LDA (MOVE_FROM),Y	;MOVE A PAGE AT A TIME
	STA VDP+0
	INY
	BNE @MD1
	INC MOVE_FROM+1
	DEX
	BNE @MD1
@MD2:	LDX MOVE_SIZE
	BEQ @MD4
@MD3:	LDA (MOVE_FROM),Y	;MOVE THE REMAINING BYTES
	STA VDP+0
	INY
	DEX
	BNE @MD3
@MD4:

WREG #%01000000, 1	;SCREEN ON
