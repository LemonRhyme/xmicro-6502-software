	LDA #$77			;MSB RED LSB BLUE
	STA VDP+2
	LDA #$07			;LSB GREEN
	STA VDP+2

	WREG #$05, 16			;BLACK COLOR 04
	LDA #$00			;MSB RED LSB BLUE
	STA VDP+2
	LDA #$00			;LSB GREEN
	STA VDP+2


;----PATTERN LAYOUT INIT----
