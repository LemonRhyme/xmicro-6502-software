	MOVE_TO:	.RES 2
	MOVE_SIZE:	.RES 2

.SEGMENT "STARTUP"

	XMICRO_MEMORY = $7100
	RTC = XMICRO_MEMORY+$10

	VDP = $7400


_INIT:
	SEI
	CLC
	LDX #$FF
	TXS

;----RTC INIT---
	LDA #%00000000		;DISABLE INTERRUPTS
	STA RTC+$0C

	JSR XM7SEG_INIT
	XM7SEG_ON
	XM7SEG_WORD #$1111

;----VDP INIT----
	WREG %00000000, 0	;$00, $80	;TEXT1 MODE
	WREG #%01010000, 1	;$50, $81

;	WREG #%00000100, 0	;$04, $80	;TEXT2 MODE
;	WREG #%01010000, 1	;$50, $81

;	WREG #%00000100, 0	;$04, $80	;MC MODE
;	WREG #%01011000, 1	;$58, $81


