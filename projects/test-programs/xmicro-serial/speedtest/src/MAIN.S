.PC02

.INCLUDE "INSTRUCTIONS.MAC"
.INCLUDE "XM7SEG.MAC"
.INCLUDE "SERIAL.MAC"

.SEGMENT "ZEROPAGE"
SECONDS_BUFFER:	.RES 2


.SEGMENT "STARTUP"

XMICRO_MEMORY = $7100
RTC = XMICRO_MEMORY+$10


_INIT:
	SEI
	CLC
	LDX #$FF
	TXS

;----RTC INIT---
	LDA #%00000000		;DISABLE INTERRUPTS
	STA RTC+$0C

	JSR SERIAL_INIT
	JSR XM7SEG_INIT
	XM7SEG_ON
	XM7SEG_WORD #$1111

	LDX #$00
	TXA
	TAY
	CLI

	LDA #$5E
	STA SERIAL_TXBUFFER
	LDA #$00
	JSR SERIAL_TX

	JMP _MAIN

.SEGMENT "CODE"

_MAIN:
	INX
	STX SERIAL_TXBUFFER
	JSR SERIAL_TX
	JMP _MAIN
