.PC02

.INCLUDE "INSTRUCTIONS.MAC"

	XMICRO_MEMORY = $7100
	RTC = XMICRO_MEMORY+$10

.SEGMENT "STARTUP"
_INIT:
	SEI			;DISABLE INTERRUPTS (DON'T RE-ENABLE)
	CLC
	LDX #$FF
	TXS

	LDA #%00000100		;TURN ON RTC
	STA RTC+$0E
	LDA #%00000001		;SET PERIODIC RATE TO 32.768KHZ
;	LDA #%00000011		;SET PERIODIC RATE TO 8.192KHZ
	STA RTC+$0B
	LDA #%00000100		;ONLY ENABLE PERIODIC INTERUPT
	STA RTC+$0C

	LDX #$00
	TXA
	TAY

	JMP _MAIN

.SEGMENT "CODE"

_MAIN:
	LDA RTC+$0D		;LOAD FLAGS REGISTER (RESET INTERRUPT FLAG)
	JMP _MAIN
